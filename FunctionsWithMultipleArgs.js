function square(num) {
    console.log(num * num);
}
// Squaring 6 will give us 36
square(6);

// defining a function with two parameters. When you call this function you must pass two arguments for the function to run.
function sum (x,y) {
    console.log(x + y);
};

// Sum should be 23
sum(5,18);

// defining a function to do simple division
function division (a, b) {
    console.log(a / b);
};
//10 divided by 5 should be 2
division(10, 5)